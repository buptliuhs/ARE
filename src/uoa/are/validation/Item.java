package uoa.are.validation;

public class Item {

    private int second;
    private String time;
    private int actA;
    private int actB;

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getActA() {
        return actA;
    }

    public void setActA(int actA) {
        this.actA = actA;
    }

    public int getActB() {
        return actB;
    }

    public void setActB(int actB) {
        this.actB = actB;
    }

}
